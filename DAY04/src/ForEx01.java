
public class ForEx01 {
	
	public static void main(String[] args) {
		//; 빼먹지말기 ! 
		//합계변수는 무조건 밖에 for문안에 만드는것 아님 !
		
		int sum = 0;
		
		
		for(int i=1; i<= 10; i++) { //while문에 3가지 필요값을 ()안에 적어놓은 형			
			sum += i;}
		System.out.println("1~10까지의 합:" + sum);
		
		
		for(int i=10; i>=1; i--) {
			System.out.println(i);
		}
		
			
			
			
			//10에서 시작해서 1로 감소하는 for문 작성해보기 
			
			
			
		
		
	}

}
