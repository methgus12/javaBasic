
public class OperatorEx04 {
	
	public static void main(String[] args) {
		
		int a = 6;
		int b = 5;
		
		a += 3;
		b =+ 3;
		
		
		System.out.println(a); // a값에 3을 더한
		System.out.println(b);//b 값을 양수 3으로바꾼다 
		
		a -= 4; //5
		System.out.println(a);
		
		a *= 10; //50
		System.out.println(a);
		
		a /=5; //10
		System.out.println(a);
		
		a %= 2; //0
		System.out.println(a);
		
		System.out.println("---------------------");
		
		
		
		
		
		
		
		
		
		
	}

}
